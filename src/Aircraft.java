class Aircraft {
    private String engine = "";
    private String seat = "";
    private String control = "";

    public void setEngine(String engine) {
        this.engine = engine;
    }

    public void setSeat(String seat) {
        this.seat = seat;
    }

    public void setControl(String control) {
        this.control = control;
    }

    public String getEngine(){
        return engine;
    }
}
